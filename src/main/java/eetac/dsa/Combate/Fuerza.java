package eetac.dsa.Combate;

/**
 * classe que represanta una fuerza a partir de sus componentes
 */
public class Fuerza {
    float x;
    float y;

    public Fuerza(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
